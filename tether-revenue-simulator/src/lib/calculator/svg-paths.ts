import type { BiddingZone } from "./types";

/**
 * Simplified SVG path data for European ENTSOE bidding zones.
 * Paths use a Mercator-like projection fit to 800x600 viewBox.
 * Covering approximately -12W to 35E, 34N to 72N.
 *
 * Projection: x = (lng + 12) * (800 / 47)
 *             y = (72 - lat) * (600 / 38)
 */

export interface SvgZonePath {
  id: BiddingZone;
  d: string;
}

export const MAP_VIEWBOX = "0 0 800 600";

// Helper: convert geo coords to SVG coords
// lng range: -12 to 35 (47 degrees) → 0 to 800
// lat range: 34 to 72 (38 degrees) → 600 to 0 (inverted)
function geo(lng: number, lat: number): string {
  const x = Math.round(((lng + 12) / 47) * 800);
  const y = Math.round(((72 - lat) / 38) * 600);
  return `${x},${y}`;
}

function geoPath(coords: [number, number][]): string {
  return coords
    .map(([lng, lat], i) => `${i === 0 ? "M" : "L"}${geo(lng, lat)}`)
    .join(" ") + " Z";
}

